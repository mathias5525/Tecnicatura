# Sentencia if/else
# La sentencia if/else se utiliza para ejecutar un bloque de código
# si se cumple una condición, o ejecutar otro bloque de código
# si no se cumple la condición.
# La sintaxis es la siguiente:
# if <condición>:   
#     <bloque de código si se cumple la condición>
# else:
#     <bloque de código si no se cumple la condición>

condicion = True  # Esta es la condición que se evalúa
if condicion:
    # Si la condición se cumple, se ejecuta este bloque de código
    print("La condición es verdadera") 
else:
    # Si la condición no se cumple, se ejecuta este bloque de código
    print("La condición es falsa")
    